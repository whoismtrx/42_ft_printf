#include "ft_printf.h"

int	ft_check_specifier(char c)
{
	if (c == 'c' || c == 's' || c == '%')
		return (1);
	if (c == 'd' || c == 'i' || c == 'u')
		return (1);
	if (c == 'x' || c == 'X' || c == 'p')
		return (1);
	return (0);
}
